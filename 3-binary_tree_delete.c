#include <stdlib.h>
#include <stdio.h>
#include "binary_trees.h"

/**
 * binary_tree_delete - Delete the tree
 *
 * @tree: binary tree
 *
 * Return: Nothing
 */
void binary_tree_delete(binary_tree_t *tree)
{
	if (tree != NULL)
	{
		tree->left == NULL;
		tree->right == NULL;
		tree->parent == NULL;
	}

}